                + ",id_cliente,id_plazo,estado,saldo_faltante,cuota_faltante) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?,?,?,?,?)";
        String query2 = "SELECT id_cliente,estado FROM tbl_prestamo";
