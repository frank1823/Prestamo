    private void btn_recuperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_recuperarActionPerformed
        String user     = txt_user.getText();
        String pass     = new String(txt_contraseña.getPassword());
        String confirm  = new String(txt_confirmacion.getPassword());
        String qry = "";
        ResultSet result;
        
        //VALIDACIONES  
        if(user.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingrese el campo Usuario", "ERROR", JOptionPane.ERROR_MESSAGE);
        }else{
            qry = "SELECT count(*) FROM tbl_usuarios WHERE usuario = ?";
            try{
                //Buscando usuario en BD Para reestablecer contraseña
                PreparedStatement stm = cnx.getConnection().prepareStatement(qry);
                stm.setString(1, user);
                result = stm.executeQuery();
                result.next();
                if(result.getString(1).equals("1")){
                    //Si el usuario existe en BD hacer update con la contraseña nueva
                    if(pass.isEmpty()){
                        JOptionPane.showMessageDialog(null, "Ingrese el campo Contraseña Nueva", "ERROR", JOptionPane.ERROR_MESSAGE);
                    }else{
                        if(confirm.isEmpty()){
                            JOptionPane.showMessageDialog(null, "Confirme la contraseña nueva", "ERROR", JOptionPane.ERROR_MESSAGE);
                        }else{
                            if(!pass.equals(confirm)){
                                JOptionPane.showMessageDialog(null, "Contraseñas no coinciden", "ERROR", JOptionPane.WARNING_MESSAGE);
                            }else{
                                qry = "UPDATE tbl_usuarios SET contraseña = ? WHERE usuario = ?";
                                PreparedStatement stm2 = cnx.getConnection().prepareStatement(qry);
                                stm2.setString(1, pass);
                                stm2.setString(2, user);
                                int upd_error = stm2.executeUpdate();
                                if(upd_error > 0){
                                    JOptionPane.showMessageDialog(null, "Contraseña Reestablecida Exitosamente!", "Recuperacion", JOptionPane.INFORMATION_MESSAGE);
                                    //Abriendo form login
                                    MainLogin mainLogin = new MainLogin();
                                    mainLogin.setVisible(true);
                                    mainLogin.setLocationRelativeTo(this);
                                    this.setVisible(false);
                                }else{
                                    JOptionPane.showMessageDialog(null, "Error al reestablecer contraseña, intente nuevamente", "ERROR", JOptionPane.ERROR_MESSAGE);
                                }
                            }
                        }
                        
                    }
                    
                    
                }else{
                    //Si el usuario no existe en BD
                    JOptionPane.showMessageDialog(null, "Usuario Incorrecto", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
                
            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex, "ERROR", JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_btn_recuperarActionPerformed

